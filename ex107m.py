def aumentar(valor):
    '''
    => Aumentar o valor passado em 10
    :param valor: valor inserido
    :return: retornar o valor atualizado
    '''
    valor += 10
    return valor


def diminuir(valor):
    '''
    => Diminuir o valor passado em 5
    :param valor: valor inserido
    :return: retornar o valor atualizado
    '''
    valor -= 5
    return valor


def dobro(valor):
    '''
    => Dobra o valor passado
    :param valor: valor inserido
    :return: retornar o valor atualizado
    '''
    valor *= 2
    return valor


def metade(valor):
    '''
    => Divide o valor passado em 2
    :param valor: valor inserido
    :return: retornar o valor atualizado
    '''
    valor = valor/2
    return valor